// ============================================================
// AuditGPT output contract — the machine-checkable half of the
// "never hallucinates / no slop" claim.
//
// This module is dependency-free on purpose: it can run in CI, in a
// script, or be imported by the app. BANNED_PHRASES here is the SINGLE
// SOURCE OF TRUTH; audit-context.ts should import this array and
// interpolate it into ANTI_SLOP_PREAMBLE so the prompt and the test
// can never disagree about what "banned" means.
// ============================================================

// Single-word bans: matched on word boundaries so "great" does not
// fire on "greater" and "delusion" does not fire on a longer word.
export const BANNED_WORDS = [
  'great',
  'exciting',
  'however',
  'delusion',
] as const;

// Multi-word bans: matched as normalized-whitespace substrings.
export const BANNED_PHRASES_MULTIWORD = [
  "let's be honest",
  'the reality is',
  'founder probably believes',
  'learn more', // banned as a CTA; flagged anywhere, see note in report
] as const;

export const INSUFFICIENT_DATA = 'insufficient data';

// Fields whose string content is subject to the 2-sentence cap and the
// banned-phrase / number checks. Arrays are checked element by element.
export const SCANNED_STRING_FIELDS = [
  'verdict',
  'verdict_header',
  'company_info',
] as const;
export const SCANNED_ARRAY_FIELDS = [
  'report_card',
  'red_flags',
  'assumptions_to_test',
  'website_fixes',
  'services_to_hire',
  'action_plan',
] as const;

export interface Violation {
  field: string;
  index: number | null; // array index, or null for scalar fields
  rule: string;
  detail: string;
  snippet: string;
}

function snip(s: string, n = 80): string {
  return s.length > n ? s.slice(0, n) + '…' : s;
}

// Collect every (field, index, text) pair the contract applies to.
function* scanText(audit: any): Generator<{ field: string; index: number | null; text: string }, void, unknown> {
  for (const f of SCANNED_STRING_FIELDS) {
    const v = audit?.[f];
    if (typeof v === 'string' && v.trim()) yield { field: f, index: null, text: v };
  }
  for (const f of SCANNED_ARRAY_FIELDS) {
    const arr = audit?.[f];
    if (Array.isArray(arr)) {
      for (let i = 0; i < arr.length; i++) {
        const v = arr[i];
        if (typeof v === 'string' && v.trim()) yield { field: f, index: i, text: v };
      }
    }
  }
}

// ── Check 1: banned phrases ─────────────────────────────────────────
export function checkBannedPhrases(audit: any): Violation[] {
  const out: Violation[] = [];
  for (const { field, index, text } of scanText(audit)) {
    const lower = text.toLowerCase().replace(/\s+/g, ' ');
    for (const w of BANNED_WORDS) {
      const re = new RegExp(`\\b${w}\\b`, 'i');
      if (re.test(text)) {
        out.push({ field, index, rule: 'banned_word', detail: w, snippet: snip(text) });
      }
    }
    for (const p of BANNED_PHRASES_MULTIWORD) {
      if (lower.includes(p)) {
        out.push({ field, index, rule: 'banned_phrase', detail: p, snippet: snip(text) });
      }
    }
  }
  return out;
}

// ── Check 2: 2-sentence cap per field ──────────────────────────────
function sentenceCount(text: string): number {
  const parts = text
    .replace(/\b(e\.g|i\.e|vs|approx|etc)\./gi, '$1<DOT>') // protect abbreviations
    .split(/[.!?]+(?:\s|$)/)
    .map((s) => s.trim())
    .filter(Boolean);
  return parts.length;
}
export function checkSentenceCap(audit: any, max = 2): Violation[] {
  const out: Violation[] = [];
  for (const { field, index, text } of scanText(audit)) {
    const n = sentenceCount(text);
    if (n > max) {
      out.push({ field, index, rule: 'sentence_cap', detail: `${n} sentences (max ${max})`, snippet: snip(text) });
    }
  }
  return out;
}

// ── Check 3: insufficient-data coverage on known-missing fields ────
// expectInsufficientData is a list of field names (or "field[]" for
// "at least one array element must say it") that MUST contain the
// literal INSUFFICIENT_DATA string given this fixture's missing inputs.
export function checkInsufficientDataCoverage(
  audit: any,
  expect: string[]
): { covered: string[]; missing: string[] } {
  const covered: string[] = [];
  const missing: string[] = [];
  const has = (s: unknown) =>
    typeof s === 'string' && s.toLowerCase().includes(INSUFFICIENT_DATA);
  for (const spec of expect) {
    const isArray = spec.endsWith('[]');
    const field = isArray ? spec.slice(0, -2) : spec;
    const v = audit?.[field];
    const ok = isArray ? Array.isArray(v) && v.some(has) : has(v);
    (ok ? covered : missing).push(spec);
  }
  return { covered, missing };
}

// ── Check 4 (ADVISORY): numbers not traceable to inputs/benchmarks ─
// Cannot prove "no hallucinated number" perfectly. This flags output
// numbers absent from the allowed set for human review. score and
// slop probability are generated by design and excluded.
export function checkUntracedNumbers(audit: any, allowed: Set<string>): Violation[] {
  const out: Violation[] = [];
  for (const { field, index, text } of scanText(audit)) {
    const nums = text.match(/\d[\d,]*\.?\d*/g) || [];
    for (const raw of nums) {
      const norm = raw.replace(/,/g, '');
      if (!allowed.has(norm) && !allowed.has(raw)) {
        out.push({ field, index, rule: 'untraced_number', detail: raw, snippet: snip(text) });
      }
    }
  }
  return out;
}

export interface ContractReport {
  bannedPhrases: Violation[];
  sentenceCap: Violation[];
  insufficientData: { covered: string[]; missing: string[] };
  untracedNumbers: Violation[]; // advisory
  pass: boolean; // hard checks only: banned + sentence cap + ID coverage
}

export function runContract(
  audit: any,
  opts: { expectInsufficientData?: string[]; allowedNumbers?: Set<string> } = {}
): ContractReport {
  const bannedPhrases = checkBannedPhrases(audit);
  const sentenceCap = checkSentenceCap(audit);
  const insufficientData = checkInsufficientDataCoverage(
    audit,
    opts.expectInsufficientData || []
  );
  const untracedNumbers = opts.allowedNumbers
    ? checkUntracedNumbers(audit, opts.allowedNumbers)
    : [];
  const pass =
    bannedPhrases.length === 0 &&
    sentenceCap.length === 0 &&
    insufficientData.missing.length === 0;
  return { bannedPhrases, sentenceCap, insufficientData, untracedNumbers, pass };
}
